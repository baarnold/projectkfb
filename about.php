                    <h3>About the Kent Food Bank</h3>
                    <p>The Kent Food Bank and Emergency Services is a non-profit organization 
                    offering food, clothing and emergency financial assistance to low income 
                    families and individuals living within the Kent School District.  The Kent
                    Food Bank service area covers 73 square miles with 40 schools. Kent
                    Food Bank operates two food distribution locations in Kent each week.</p>
                    <h3>Mission Statement</h3>
                    <p>The Kent Food Bank and Emergency Services exists to serve the
                    citizens of the Kent area.  In order to fulfill it's mission the Kent Food
                    Bank will: Operate a center to supply food, clothing and referrals to
                    persons in need living within the service area.  Furnish financial
                    assistance in an emergency situation for emergency needs.  Work in
                    conjunction with government agencies, churches and other organizations
                    to serve the surrounding people.  Operate primarily as a volunteer agency,
                    obtaining the majority of its support and donations from the community.</p>
                    <h3>Cultural Competency and Accessibility</h3>
                    <p>197 are spoken in the Kent School District, making it one the country’s most diverse. Kent Food Bank
                    actively works to ensure that this diverse population is reflected in the programs it offers. Volunteers at
                    the Food Bank are diverse and speak a number of different languages, providing translations services to
                    customers as needed. Printed materials are also available in several languages.</p>
                    <h3>Collaboration</h3>
                    <p>The Kent Food Bank partners with WithinReach to provide onsite public benefits enrollment for
                    customers. Northwest Harvest, the LDS cannery and the South King County Food Coalition are working
                    together to help can food. Northwest Harvest is providing the food, the LDS cannery is providing the
                    equipment, cans, boxes and labels, the South King County Food Banks are providing the labor. The food
                    banks of South King County will receive the canned product for distribution. </p>
                    <h3>Financial Health</h3>
                    <p>As the only organization of its kind in the area, The Kent Food Bank receives a great deal of support from
                    the local community, including the City of Kent, the Kent High School District, and local businesses. 60%
                    of the organization’s operating budget comes from individuals and businesses, including 35 churches, 40
                    civic organizations, and 80 local businesses.</p>
